export interface DailyQuote {
  text: string
  author: string
}

/** 날짜 + 사용자 시드 기반 결정적 명언 선택 (같은 날 같은 사람 = 같은 명언, 다른 사람 = 다른 명언) */
export function getDailyQuote(quotes: DailyQuote[], userSeed: string): DailyQuote {
  const now = new Date()
  const dateStr = `${now.getFullYear()}-${String(now.getMonth() + 1).padStart(2, '0')}-${String(now.getDate()).padStart(2, '0')}`
  const key = `${dateStr}:${userSeed}`
  let hash = 0
  for (let i = 0; i < key.length; i++) {
    hash = ((hash << 5) - hash + key.charCodeAt(i)) | 0
  }
  return quotes[Math.abs(hash) % quotes.length]
}

/** localStorage에 저장되는 사용자별 고유 시드 (최초 1회 생성) */
const USER_SEED_KEY = 'gillog-quote-seed'
export function getUserSeed(): string {
  if (typeof window === 'undefined') return 'default'
  let seed = localStorage.getItem(USER_SEED_KEY)
  if (!seed) {
    seed = `${Date.now()}-${Math.random().toString(36).substr(2, 12)}`
    localStorage.setItem(USER_SEED_KEY, seed)
  }
  return seed
}

